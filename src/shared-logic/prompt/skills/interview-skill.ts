/**
 * Interview Skill — 模糊想法 → 创作简报（Task 1.4 v5.3 增强）
 *
 * 借鉴 seedance-2.0（MIT 许可）的 seedance-interview SKILL 模式。
 *
 * 触发场景：用户输入模糊（"我想做个视频"/"不知道拍什么"/"帮我想想"）。
 * 行为：引导 AI 先做 3-5 个简短创意问题（题材/风格/角色/情绪/时长），
 *       而非直接 dump 一堆 prompt 模板。
 *
 * 本文件属于 shared-logic 层，零外部依赖。
 */

import type { AgentContext, Skill } from "./index";

export const interviewSkill: Skill = {
  id: "interview",
  matchers: [
    "我想做",
    "不知道",
    "随便",
    "帮我想",
    "没想法",
    "有什么",
    "建议",
    "推荐",
    "i want to make",
    "no idea",
    "help me",
    "suggest",
  ],

  buildInstructions(ctx: AgentContext): string {
    const projectTypeHint = ctx.projectType && ctx.projectType !== "unknown"
      ? `\n- 用户当前项目类型为「${ctx.projectType}」，问题可围绕该类型展开（如古装→朝代/服饰风格；科幻→科技等级/未来设定）`
      : "";

    return [
      "## 当前模式：创意访谈（Interview）",
      "",
      "用户输入较为模糊，尚不清楚具体创作意图。请先进行 3-5 个简短的创意问答，帮助用户明确想法，**不要直接输出 prompt**。",
      "",
      "访谈维度（按需选择 3-5 个，避免一次问太多）：",
      "1. **题材/类型**：故事类型（如现代都市/古装武侠/科幻未来/奇幻冒险）",
      "2. **视觉风格**：期望的画面风格（如写实/日系动画/水墨/赛博朋克/电影质感）",
      "3. **核心角色**：主要角色数量与特征（如单人/双人/群像；性别/年龄/服饰）",
      "4. **情绪氛围**：期望传达的情绪（如紧张/温馨/神秘/热血/忧郁）",
      "5. **时长/节奏**：视频时长与节奏（如 5 秒短镜头/15 秒叙事/30 秒完整场景）",
      "",
      "访谈规则：",
      "- 每次只问 1 个问题，等用户回答后再问下一个（避免列表式轰炸）",
      "- 问题要具体且有选项感（如「现代都市还是古装武侠？」而非「什么题材？」）",
      "- 根据用户已有回答动态调整后续问题（如用户已选古装，下一个问朝代而非题材）",
      "- 收集到 3-5 个维度信息后，主动总结为「创作简报」并询问是否开始生成",
      "- 用户中途明确表达「直接生成」时，尊重用户意愿，跳过剩余问题",
      projectTypeHint,
    ].filter(Boolean).join("\n");
  },
};
